/*
Q.2: Take two variables and store age then using if/else condition to determine
oldest and youngest among them.
*/
void main() {
  int age1 = 24;
  int age2 = 29;
  if (age1 > age2) {
    print('Age1 = $age1 is oldest and Age2 = $age2 is youngest');
  } else {
    print('Age2 = $age2 is oldest and Age1 = $age1 is youngest');
  }
}
