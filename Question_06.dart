//Q.6: Write a program to check whether an alphabet is a vowel or consonant.
void main() {
  String Alphabet = 'a';
  if (Alphabet == 'a' ||
      Alphabet == 'e' ||
      Alphabet == 'i' ||
      Alphabet == 'o' ||
      Alphabet == 'u') {
    print('Alphabet $Alphabet is vowel');
  } else {
    print('The Alphabet $Alphabet is consonant');
  }
}
